<div class="modal-body">
    <h2>Caching</h2>
    <p>
        On slow servers the creation of big lists can take several seconds.<br>
        To speed this up, it is possible to cache generates lists and use them next time again without new creation.
    </p>
    <h2>Flushing</h2>
    <p>
        When the cached files reaching the maximum cache size, the longest not used cached files get deleted to free space for new files.
    </p>
</div>

